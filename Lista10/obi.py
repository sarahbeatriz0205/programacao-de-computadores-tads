num_competidores, pontos_minimos = map(int, input().split())
pontos_fase1 = []
pontos_fase2 = []
participantes_aprovados = 0

# inserção da qtd de pontos (errada)
for i in range(num_competidores):
    pontos_fase1.append, pontos_fase2.append = map(int, input().split())

for i in range(num_competidores):
    soma = pontos_fase1[i] + pontos_fase2[i]
    if soma >= pontos_minimos:
        participantes_aprovados += 1

print(pontos_fase1)